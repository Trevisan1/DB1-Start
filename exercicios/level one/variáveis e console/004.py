primeira_nota = int(input('Digite a note do primeiro bimestre '))
segunda_nota = int(input('Digite a note do segundo bimestre '))
terceira_nota = int(input('Digite a note do terceiro bimestre '))
quarta_nota = int(input('Digite a note do quarto bimestre '))

